        # Update title with proper HTML formatting
        $content = $content -replace "(?s)<title>.*?</title>", "<title>$($gameInfo[$game].title)</title>"
        
        # Update meta description with proper HTML formatting
        $content = $content -replace '(?s)<meta\s+name="description"[^>]*>', "<meta name=`"description`" content=`"$($gameInfo[$game].description)`">"
        
        # Clean up any duplicate meta tags
        $content = $content -replace '(?s)<meta\s+name="description"[^>]*>(\s*<meta\s+name="description"[^>]*>)+', "<meta name=`"description`" content=`"$($gameInfo[$game].description)`">"
        
        # Update h1 title (if exists)
        if ($content -match "<h1>.*?</h1>") {
            $gameName = $game -replace "-", " "
